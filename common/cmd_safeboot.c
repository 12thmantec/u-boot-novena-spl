/*
 * Subroutine:  do_bmp
 *
 * Description: Handler for 'bmp' command..
 *
 * Inputs:	argv[1] contains the subcommand
 *
 * Return:      None
 *
 */
int do_safeboot(cmd_tbl_t *cmdtp, int flag, int argc, char * const argv[])
{
	ulong dev, nsys, bsys, len, laddr, kaddr, raddr;
	char command[256];
	char *knob_state;
	int rc;
	
	if (argc < 8) {
		cmd_usage(cmdtp);
		return 1;
	}

	dev   = simple_strtoul(argv[1], NULL, 10);
	nsys  = simple_strtoul(argv[2], NULL, 16);
	bsys  = simple_strtoul(argv[3], NULL, 16);
	len   = simple_strtoul(argv[4], NULL, 16);
	laddr = simple_strtoul(argv[5], NULL, 16);
	kaddr = simple_strtoul(argv[6], NULL, 16);
	raddr = simple_strtoul(argv[7], NULL, 16);
#ifdef CONFIG_VIDEO
	video_clear();

	bmp_display((ulong)bmp_12th_man, 
			(800 - 160) / 2,
			(600 - 35) / 2
			);

	if ((knob_state = getenv("knob_switch_pressed")) != NULL && *knob_state == '1') {
		bmp_display((ulong)bmp_diagnostic_mode, 603, 445);
	}
	lcd_enable_backlight(1);

#endif
	sprintf(command, "mmc read %d 0x%x 0x%x 0x%x", dev, laddr, nsys, len);
	rc = run_command(command, 0);

	sprintf(command, "bootz 0x%x 0x%x", kaddr, raddr);
	rc = run_command(command, 0);

	/* we should never reach here, if run at here, it means fail 
	 * to boot the normal system, so we should attempt to load the
	 * backup system.
	 */
	sprintf(command, "mmc read %d 0x%x 0x%x 0x%x", dev, laddr, bsys, len);
	run_command(command, 0);

	sprintf(command, "bootz 0x%x 0x%x", kaddr, raddr);
	run_command(command, 0);

	return 0;
}

U_BOOT_CMD(
	safeboot,	8,	1,	do_safeboot,
	"Load and boot system, if fail, load backup system to boot",
	"dev nsys bsys len laddr kaddr raddr\n"
	"dev            the device to be load\n"
	"nsys           the start block of normal system in 'dev'\n"
	"bsys           the start block of backup system in 'dev'\n"
	"len            the total blocks of system\n"
	"laddr          the dram address which system will be loaded\n"
	"kaddr          the kernel address in dram\n"
	"raddr          the rootfs address in dram\n"
);

